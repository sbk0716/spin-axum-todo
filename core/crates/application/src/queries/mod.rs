// =============================================================================
// application/src/queries/mod.rs: クエリ（参照操作）モジュール
// =============================================================================
// 軽量 CQRS パターン: Queries は状態を読み取る操作を担当。
// Reader DB プールを使用する。
//
// Commands との違い:
// - Commands: 状態を変更する（Create, Update, Delete）→ Writer 使用
// - Queries: 状態を読み取る（Get, List）→ Reader 使用
//
// Reader と Writer を分離する理由:
// - 読み取り専用レプリカに対応可能（スケーラビリティ向上）
// - 読み取りと書き込みの負荷を分散
// - 将来的にキャッシュを透過的に導入可能
// =============================================================================

// -----------------------------------------------------------------------------
// サブモジュールの宣言
// -----------------------------------------------------------------------------

