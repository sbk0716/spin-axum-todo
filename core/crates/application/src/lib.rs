// =============================================================================
// application/src/lib.rs: アプリケーション層のルートモジュール
// =============================================================================
// クリーンアーキテクチャのアプリケーション層（ユースケース層）。
// ビジネスロジックのオーケストレーションを担当する。
//
// アプリケーション層の責務:
// 1. ユースケースの実装（Commands / Queries）
// 2. トランザクション境界の管理
// 3. DTO（Data Transfer Object）の定義
// 4. ドメイン層とプレゼンテーション層の橋渡し
//
// 軽量 CQRS パターン:
// - Commands: 状態変更操作（Create, Update, Delete）- TodoWriter 使用
// - Queries: 参照操作（Get, List）- TodoReader 使用
//
// 依存関係:
// ```text
// presentation → application → domain
//                    ↑
//               ユースケースを実装
// ```
//
// Rust のモジュール公開ルール:
// - `pub mod` でサブモジュールを公開
// - `pub use` で内部の型をクレートルートから直接アクセス可能に
// =============================================================================

// =============================================================================
// モジュール宣言
// =============================================================================

/// コマンドモジュール（状態変更操作）
/// CQRS の Command 側を担当
pub mod commands;

/// DTO モジュール（Data Transfer Object）
/// 外部との入出力データ構造を定義
pub mod dto;

/// クエリモジュール（参照操作）
/// CQRS の Query 側を担当
pub mod queries;

/// サービスモジュール
/// 認証などの横断的関心事を処理
pub mod services;

// =============================================================================
// 再エクスポート（Re-export）
// =============================================================================
// 使用側で `application::CreateTodoCommand` のように直接アクセス可能にする。
// =============================================================================

/// Commands を再エクスポート
pub use commands::*;

/// Queries を再エクスポート
pub use queries::*;

/// DTO を再エクスポート
pub use dto::*;

/// サービスを再エクスポート
pub use services::*;
