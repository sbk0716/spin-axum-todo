// =============================================================================
// domain/src/errors/mod.rs: ドメインエラーモジュール
// =============================================================================
// ドメイン層で発生するエラーを定義するモジュール。
//
// Rust のモジュールシステム:
// - `mod domain_error;` で同ディレクトリの domain_error.rs を読み込む
// - `pub use` で DomainError を親モジュールからアクセス可能にする
//
// これにより、外部から以下のようにアクセスできる:
// - domain::errors::DomainError
// - domain::DomainError（lib.rs で再エクスポート）
// =============================================================================

// -----------------------------------------------------------------------------
// サブモジュールの宣言
// -----------------------------------------------------------------------------

/// ドメインエラー型を定義するモジュール
/// domain_error.rs ファイルを読み込む
mod domain_error;

// -----------------------------------------------------------------------------
// 再エクスポート（Re-export）
// -----------------------------------------------------------------------------

/// DomainError を公開エクスポート
/// pub use により、domain::errors::domain_error::DomainError ではなく
/// domain::errors::DomainError として直接アクセス可能になる
pub use domain_error::DomainError;
