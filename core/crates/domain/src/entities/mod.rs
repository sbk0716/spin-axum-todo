// =============================================================================
// domain/src/entities/mod.rs: エンティティモジュール
// =============================================================================
// クリーンアーキテクチャにおける「エンティティ」を定義するモジュール。
//
// エンティティとは:
// - ビジネスドメインの中核となるデータ構造
// - ビジネスルール（バリデーション等）をカプセル化
// - 外部フレームワーク（axum、sqlx）に依存しない純粋な Rust 構造体
//
// このプロジェクトのエンティティ:
// - Todo: TODO アイテム（タスク管理の中心）
// - User: ユーザー（認証・認可の主体）
// - File: 添付ファイル（TODO に紐づくファイルメタデータ）
// =============================================================================

// -----------------------------------------------------------------------------
// サブモジュールの宣言
// -----------------------------------------------------------------------------
// Rust では mod キーワードでサブモジュールを宣言する。
// これにより、同じディレクトリ内の file.rs、todo.rs、user.rs が読み込まれる。
// -----------------------------------------------------------------------------

/// File エンティティを定義するモジュール
mod file;

/// Todo エンティティを定義するモジュール
mod todo;

/// User エンティティを定義するモジュール
mod user;

// -----------------------------------------------------------------------------
// 再エクスポート（Re-export）
// -----------------------------------------------------------------------------
// pub use により、サブモジュール内の型を親モジュールから直接アクセス可能にする。
// 例: domain::entities::file::File → domain::entities::File
// さらに lib.rs で再エクスポートすると domain::File でアクセス可能になる。
// -----------------------------------------------------------------------------

/// File エンティティを再エクスポート
pub use file::File;

/// Todo エンティティを再エクスポート
pub use todo::Todo;

/// User エンティティを再エクスポート
pub use user::User;
